package traintickets_booking_1;

public class Passengers {

	public String name;
	public int age;
	public String gender;
	public String berth_preference;
	public Passengers(String name, int age, String gender, String berth_preference) {
		super();
		this.name = name;
		this.age = age;
		this.gender = gender;
		this.berth_preference = berth_preference;
	}
	
	
}
